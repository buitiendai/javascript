let width = prompt("Nhập vào chiều dài của hình chữ nhật:");
let height = prompt("Nhập vào chiều rộng của hình chữ nhật:");
let a = parseInt(width);
let b = parseInt(height);
let S = a*b;
//Hiển thị các giá trị:
document.write('Chiều dài cạnh hình chữ nhật là: '+ width + 'm' + '<br />');
document.write('Chiều rộng cạnh hình chữ nhật là: '+ height + 'm' + '<br />');
document.write('Diện tích hình chữ nhật là: '+ S + 'm2' );